/// Unicode character width lookup (wcwidth equivalent).
///
/// Uses Unicode 15.1 East Asian Width data. Binary search for codepoints
/// above ASCII; inline fast-path for printable ASCII.
library;

/// Returns the display width of [codepoint]:
///   0 for zero-width (combining marks, controls, etc.)
///   1 for normal-width
///   2 for wide (CJK, fullwidth, emoji)
///  -1 for non-printable control characters
@pragma('vm:prefer-inline')
int unicodeWidth(int codepoint) {
  // Fast path: ASCII
  if (codepoint < 0x20) return -1; // C0 controls
  if (codepoint < 0x7F) return 1; // Printable ASCII
  if (codepoint == 0x7F) return -1; // DEL

  // C1 controls
  if (codepoint >= 0x80 && codepoint < 0xA0) return -1;

  // Soft hyphen
  if (codepoint == 0xAD) return 1;

  // Check zero-width ranges first (combining marks, ZWJ, etc.)
  if (_binarySearch(_zeroWidth, codepoint)) return 0;

  // Check wide ranges (CJK, fullwidth, emoji)
  if (_binarySearch(_wide, codepoint)) return 2;

  // Default: normal width
  return 1;
}

/// Binary search in a sorted array of [start, end] pairs.
bool _binarySearch(List<int> ranges, int cp) {
  var lo = 0;
  var hi = ranges.length ~/ 2 - 1;
  while (lo <= hi) {
    final mid = (lo + hi) ~/ 2;
    final start = ranges[mid * 2];
    final end = ranges[mid * 2 + 1];
    if (cp < start) {
      hi = mid - 1;
    } else if (cp > end) {
      lo = mid + 1;
    } else {
      return true;
    }
  }
  return false;
}

